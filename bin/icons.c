void
init_pixmaps (GtkWidget *window)
/*
 *  Initialize builtin pixmaps.
 *
 *  No return value.
 *
 *  Side effects:
 *	global pixmap array 'p_array' is allocated and filled with all
 *	available pixmaps
 */
{
   char **pmap[] = {undo_cap_up_xpm, bibtex_xpm, info_prev_xpm,
		    info_next_xpm, view_xpm, cut_cap_up_xpm};
   unsigned n;
   
   p_array = Calloc (P_LAST, sizeof (pixmap_t));

   for (n = 0; n < P_LAST; n++)
   {
      p_array [n].pixmap = gdk_pixmap_create_from_xpm_d (window->window,
							 &p_array [n].mask,
							 NULL, pmap [n]);
      gdk_pixmap_ref (p_array [n].pixmap);
      gdk_bitmap_ref (p_array [n].mask);
   }
}

